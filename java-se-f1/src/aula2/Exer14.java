package aula2;

public class Exer14 {

	public static void main(String[] args) {

		int a = 10;
		a = a + a;
		System.out.println(a);
		
		a = 10;
		a += a;
		System.out.println(a);
		
		a = 10;
		a -= a;
		System.out.println(a);
		
		a = 10;
		a *= a;
		System.out.println(a);
		
		a = 10;
		a /= a;
		System.out.println(a);
		
		a = 10;
		a %= a;
		System.out.println(a);
	}

}
